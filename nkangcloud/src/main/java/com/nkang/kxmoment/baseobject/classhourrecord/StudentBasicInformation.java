package com.nkang.kxmoment.baseobject.classhourrecord;

public class StudentBasicInformation {
	public String name;
	public String phoneNumber;
	public String registerTime;
	public String registerWay;
	public String schoolSite;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getRegisterTime() {
		return registerTime;
	}
	public void setRegisterTime(String registerTime) {
		this.registerTime = registerTime;
	}
	public String getRegisterWay() {
		return registerWay;
	}
	public void setRegisterWay(String registerWay) {
		this.registerWay = registerWay;
	}
	public String getSchoolSite() {
		return schoolSite;
	}
	public void setSchoolSite(String schoolSite) {
		this.schoolSite = schoolSite;
	}
	
}
